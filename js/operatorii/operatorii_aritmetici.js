let x = 10;
let y = 5;

// =============================================
// (1) OP. DE  'ADAUGARE' (EXPRESIE):
console.log(x + y);

// =============================================
// (2) OP. DE  'MULTIPLICARE' (EXPRESIE):
console.log(x * y);

// =============================================
// (3) OP.  DE  'DIVIZARE' (EXPRESIE):
console.log(x / y);

// =============================================
// (4) OP.  DE  'RESTURI'/'REMAINDERUP' (EXPRESIE):
console.log(x % y);

// =============================================
// (5) OP.  DE  'EXPONENTIERE'/'EXPONENTIATION' SAU
//              'RIDICARE LA PATRAT' (EXPRESIE):
console.log(x ** y);

