/* ==========================================
    OPERATORII  DE  'ATRIBUIRE'
=============================================*/

let x = 10;

// =============================================
// (1) 'ATRIBUIRE' PRIN 'INCREMENTARE':
x++;
x = x + 1;


// =============================================
// (2) ARTIBUIREA 'ADUNARI':
x = x + 5;
x += 5;


// =============================================
// (3) ARTIBUIREA 'INMULTIRII':
x = x * 2;
x *= 2;



// =============================================
// (4) ARTIBUIREA PRIN 'DIVIZARE':
x = x / 1;
x /= 1;


// =============================================
// (5) ARTIBUIREA PRIN 'RESTURI':
x = x % 4;
x %= 4;