/* ==========================================
    OPERATORII  DE  'COMPARATIE'
=============================================*/

/*
    __________________________________________________
    * Utilizam 'Operatori de Comparatie'  impreuna  cu  'Variabile'  & 'Constantele'
            ==> pentru a Compara 'Valoare' unei 'Variabile'
            ==> cu 'Altceva'. 
    * Rezultatul unei 'Comparatii' ==> este 'boolean' (Adevarat sau Fals).  
     __________________________________________________
*/

let x = 1;

// =============================================
// (1.1) OP. 'RELATIONAL' - 'MAI MARE DECAT' ('>'):
console.log(x > 0);

// =============================================
// (1.2) OP. 'RELATIONAL' - 'MAI MARE DECAT' SAU 'EGAL CU' ('<='):
console.log(x >= 1);

// =============================================
// (1.3) OP. 'RELATIONAL' - 'MAI MIC DECAT' ('<'):
console.log(x < 1);

// =============================================
// (1.4) OP. 'RELATIONAL' - 'MAI MIC DECAT' SAU 'EGAL CU' ('<='):
console.log(x <= 1);

// =============================================
// (2.1) OP. DE 'EGALITATE' - 'EGAL CU' ('==='):
console.log(x === 1);

// =============================================
// (2.2) OP. DE 'EGALITATE' - 'DIFERIT DE' ('!=='):
console.log(x !== 1);